async function solveCaptcha(page, target) {
  try {
    const captcha = await page.$('[data-sitekey]');
    if (captcha) {
      const siteKey = await captcha.getAttribute('data-sitekey');
      const response = await solver.recaptcha({ pageUrl: target, siteKey });
      await page.evaluate(token => {
        document.querySelector('[data-sitekey]').setAttribute('data-response', token);
        const textarea = document.querySelector('textarea[name="g-recaptcha-response"]') || document.querySelector('textarea[name="h-captcha-response"]');
        if (textarea) textarea.innerText = token;
      }, response.data);
      return response.data;
    }
  } catch (error) {
    console.log(`${getCurrentTime()} [Captcha] Error: ${error.message}`);
  }
}

async function captureHAR(target) {
  try {
    const browser = await puppeteer.launch({ headless: true });
    const page = await browser.newPage();
    const har = [];
    page.on('request', request => {
      har.push({
        method: request.method(),
        url: request.url(),
        headers: request.headers(),
        postData: request.postData() || null,
      });
      request.continue();
    });
    await page.goto(target, { waitUntil: 'networkidle0', timeout: 10000 });
    await browser.close();
    return har;
  } catch (error) {
    console.log(`${getCurrentTime()} [HAR] Error: ${error.message}`);
    return [];
  }
}

