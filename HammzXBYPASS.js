async function getStatus() {
  const timeoutPromise = new Promise((_, reject) => {
    setTimeout(() => reject(new Error('Request Timed Out')), 5000);
  });

  try {
    const response = await Promise.race([axios.get(targetURL, { httpsAgent: agent }), timeoutPromise]);
    console.log(`${getCurrentTime()} [HTTP-X]  Title: ${getTitleFromHTML(response.data)} (\x1b[32m${response.status}\x1b[0m)`);
  } catch (error) {
    if (error.message === 'Request Timed Out') {
      console.log(`${getCurrentTime()} [HTTP-X]  Request Timed Out`);
    } else if (error.response) {
      console.log(`${getCurrentTime()} [HTTP-X]  Title: ${getTitleFromHTML(error.response.data)}`);
    } else {
      console.log(`${getCurrentTime()} [HTTP-X]  ${error.message}`);
    }
  }
}

